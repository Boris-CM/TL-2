package modelo;

public class Audio {
	private int datos;
	private int tiempo;
	private int duracion;

	// ----- Getters & Setters -----
	
	public int getDatos() {
		return datos;
	}
	
	public void setDatos(int datos) {
		this.datos = datos;
	}
	
	public int getTiempo() {
		return tiempo;
	}
	
	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}
	
	public int getDuracion() {
		return duracion;
	}
	
	public void setDuracion(int duracion) {
		this.duracion = duracion;
	}
}
